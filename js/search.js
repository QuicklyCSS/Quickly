let currentIndex = -1;
function buscar() {
  const input = document.getElementById("search-input").value.toLowerCase();
  const lista = document.getElementById("suggestions-list");
  lista.innerHTML = "";
  currentIndex = -1;
  if (input.length >= 3) {
    const coincidencias = sugerencias.filter((sugerencia) =>
      sugerencia.nombres.some((nombre) => nombre.toLowerCase().includes(input))
    );
    if (coincidencias.length > 0) {
      lista.style.display = "block";
      coincidencias.forEach((coincidencia, index) => {
        const elemento = document.createElement("li");
        elemento.textContent = coincidencia.nombres[0];
        elemento.setAttribute("tabindex", index);
        elemento.onclick = () => {
          document.getElementById("search-input").value =
            coincidencia.nombres[0];
          lista.style.display = "none";
          window.location.href = coincidencia.url;
        };
        lista.appendChild(elemento);
      });
    } else {
      lista.style.display = "none";
    }
  } else {
    lista.style.display = "none";
  }
}
function setActive(items) {
  Array.from(items).forEach((item) => {
    item.classList.remove("active");
  });
  if (items[currentIndex]) {
    items[currentIndex].classList.add("active");
    document.getElementById("search-input").value =
      items[currentIndex].textContent;
  }
}
function ir() {
  const inputBusqueda = document.getElementById("search-input").value.trim();
  const searchButton = document.getElementById("search-button");
  if (inputBusqueda === "") {
    mostrarModal("caso1");
  } else if (!hayCoincidencias(inputBusqueda)) {
    mostrarModal("caso2");
  } else {
    const coincidencia = sugerencias.find((sugerencia) =>
      sugerencia.nombres.some((nombre) =>
        nombre.toLowerCase().includes(inputBusqueda.toLowerCase())
      )
    );
    if (coincidencia) {
      searchButton.removeAttribute("data-Qy-toggle");
      window.location.href = coincidencia.url;
    } else {
      mostrarModal("caso2");
    }
  }
}
