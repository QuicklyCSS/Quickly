function hayCoincidencias(consulta) {
  const inputLower = consulta.toLowerCase();
  return sugerencias.some((sugerencia) =>
    sugerencia.nombres.some((nombre) =>
      nombre.toLowerCase().includes(inputLower)
    )
  );
}
